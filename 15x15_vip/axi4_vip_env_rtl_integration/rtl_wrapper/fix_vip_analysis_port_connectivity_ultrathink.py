                    tx.awlen = len;
                    tx.awid = id;
                    tx.awsize = vif.awsize;
                    tx.awburst = vif.awburst;
                    tx.awlock = vif.awlock;
                    tx.awcache = vif.awcache;
                    tx.awprot = vif.awprot;
                    tx.awqos = vif.awqos;
                    tx.awregion = vif.awregion;
                    
